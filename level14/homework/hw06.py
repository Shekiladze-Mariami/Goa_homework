# 6)შეამოწმეთ შეყვანილი სიტყვა hello-ა თუ hello world. 7) ახსენით ყველაფერი კომენტარებით

word = input("enter your word: ")

if word == "hello":
    print("დაიბეჭდა სიტყვა hello")

elif word == "hello world":
    print("დაიბეჭდა hello world")

elif word != "hello" or word != "hello world":
    print("ვერ დაიბეჭდა ზემოთ ნახსენები სიტყვები")

